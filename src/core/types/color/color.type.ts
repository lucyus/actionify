export type Color = {

  /**
   * @description The red component of the pixel in the integer range [0, 255].
   */
  red: number;

  /**
   * @description The green component of the pixel in the integer range [0, 255].
   */
  green: number;

  /**
   * @description The blue component of the pixel in the integer range [0, 255].
   */
  blue: number;

  /**
   * @description The alpha component of the pixel in the real range [0, 1].
   */
  alpha: number;

}
